///////////////////////////////////////////////////////////////
/// Constructor
///////////////////////////////////////////////////////////////
function Circle(inRadius)
{
    DEBUGCheckArgumentsAreValids(arguments, 1);

    this.type = "circle";

    this.center = new Vector2D(0, 0);
    this.radius = inRadius;
}

///////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////
Circle.prototype.drawPath = function()
{
    drawCirclePath(this.center, this.radius);
}

///////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////
Circle.prototype.draw = function()
{
    gCtx.save();
    this.drawPath();
    gCtx.fillStyle = "#FF0000";
    gCtx.fill();
    gCtx.restore();
}

///////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////
Circle.prototype.translate = function(inOffset)
{
    DEBUGCheckArgumentsAreValids(arguments, 1);

    this.center.addInline(inOffset);
}

///////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////
Circle.prototype.scale = function(inRatio)
{
    DEBUGCheckArgumentsAreValids(arguments, 1);

    this.radius *= inRatio;
}
