"use strict";

/**
 * @constructor
 */
function Shape()
{
    this.type = "uninitialized_shape";
}

///////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////
Shape.prototype.init = function(inType)
{
    DEBUGCheckArgumentsAreValids(arguments, 1);

    this.type = inType;
}

///////////////////////////////////////////////////////////////
// Must be redefined by sub class
///////////////////////////////////////////////////////////////
Shape.prototype.translate = function(inOffset)
{
    console.error("Translation not defined for shape ", inType);
}

///////////////////////////////////////////////////////////////
// Must be redefined by sub class
///////////////////////////////////////////////////////////////
Shape.prototype.rotate = function(inAngle)
{
    console.error("Rotation not defined for shape ", inType);
}

///////////////////////////////////////////////////////////////
// Must be redefined by sub class
///////////////////////////////////////////////////////////////
Shape.prototype.scale = function(inRatio)
{
    console.error("Scale not defined for shape ", inType);
}

///////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////
Shape.prototype.setPosition = function(inNewPosition)
{
    DEBUGCheckArgumentsAreValids(arguments, 1);

    this.translate(inNewPosition.sub(this.position));
}

///////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////
Shape.prototype.setAngle = function(inNewAngle)
{
    DEBUGCheckArgumentsAreValids(arguments, 1);

    this.rotate(inNewAngle - this.angle);
}
